function generateLi(blog) {
  const name = blog._text;
  const feedUrl = blog._xmlUrl;
  const siteUrl = blog._htmlUrl;
  const description = blog._description;

  if (!name || !feedUrl || !siteUrl || !description) {
    return false;
  }

  const div = `<div class="card"><h3>${name}</h3> <p>${description}</p> <a href="${siteUrl}">Site</a><a href="${feedUrl}">Feed</a></div>`;

  return div;
}

function generateBlogroll(blogroll) {
  let ul = ``;
  blogroll.forEach((blog) => {
    const li = generateLi(blog);
    if (li) {
      ul += li;
    }
  });
  return ul;
}

const app = document.querySelector("#app");

app.innerHTML = generateBlogroll(blogroll);
